#ifndef RADWM_TYPES_H
#define RADWM_TYPES_H

#include <stdint.h>
#include <stddef.h>

typedef uint8_t u8; /* unsigned char */
typedef uint16_t u16; /* unsigned short */
typedef uint32_t u32; /* unsigned int */
typedef uint64_t u64; /* unsigned long long */

typedef int8_t i8; /* signed char */
typedef int16_t i16; /* short */
typedef int32_t i32; /* int */
typedef int64_t i64; /* long long */

typedef float f32;
typedef double f64;

typedef size_t usize; /* unsigned long */
typedef ptrdiff_t isize; /* signed long */

#endif
